package ex1;

/**
 * Classe permettant d'effectuer les différents calculs
 */
public class Calcul {

	/**
	 * Méthode addition permettant l'addition entre 2 entiers
	 * @param a entier 1
	 * @param b entier 2
	 * @return retourne a+b
	 */
	public static int addition(int a, int b){
		return a+b;
	}

	/**
	 * Méthode Soustraction  permettant la soustraction entre 2 entiers
	 * @param a entier 1
	 * @param b entier 2
	 * @return retourne a-b
	 */
	public static int soustraction (int a, int b){
		return a-b;
	}
}
